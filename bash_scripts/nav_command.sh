cd
change directories.
cd -
switch back to the formal directory.
cd ..
it moves you back to the parent directory.
cd ~
it takes you from current working directory to user home directory.
pwd
it makes you print working directory.
mv
it helps you move or rename files and folders.
cp
it helps you copy files and directories fro one location to another.
ls
it helps you list content in working directory.
ls -a
it helps you list all hiden files and non-hiden files.
ls -al
it helps you list all files and folder in a long format.
ls -lan
it also hleps you list files and folder in a long format but it will also show the hidden files to.

 